# Operators

# Arithmetic operators (+,-,*,/,%,**,//)
# Assignment operators (=,+=,-=,*=,/=,**=,//=,%=)
# Comparison operators (>=,<=,==,>,<,!=)
# Logical operators (and, or, not)
# Identity operators (is, is not)
# Membership operators (in, not in)
# Bitwise operators (&, |, ^, ~, >>, <<)

x = 2 
y=1
print(x&y)
print(x|y)
print(x^y)
print(~x)
print(x>>y)
print(x<<y)

l = [2,4,6,8]
print(2 in l)
print(2 not in l)

print(x is y)
print(x is not y)

x += 10 # x=x+10
print(x)